#include "../includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		cnt;

	if (!s)
		return (NULL);
	str = (char *)malloc(sizeof(*s) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	cnt = 0;
	while (s[cnt])
	{
		str[cnt] = (*f)(cnt, s[cnt]);
		cnt++;
	}
	str[cnt] = 0;
	return (str);
}
